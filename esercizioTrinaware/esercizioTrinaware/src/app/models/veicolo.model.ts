export class Veicolo{

    modello: String;
    marca: String;
    cavalli: Number;
    prezzo: Number;

    constructor(modello: String, marca: String, cavalli: Number, prezzo: Number){
        this.modello = modello;
        this.marca = marca;
        this.cavalli = cavalli;
        this.prezzo = prezzo;
    }

}